package com.hotel.domain.entity;

/**
 * Enumeration representing room types available in hotels.
 */
public enum RoomType {
    SINGLE,      // Single occupancy room
    DOUBLE,      // Double occupancy with one bed
    TWIN,        // Double occupancy with two beds
    SUITE,       // Larger room with separate living area
    DELUXE,      // Premium room with extra amenities
    FAMILY,      // Room suitable for families
    PRESIDENTIAL // Top-tier luxury accommodation
}
